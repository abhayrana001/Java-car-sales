void fetchData(String uname)
{ 
        try
        {
            //	name	phone	gender	dob	address	username	password	usertype 
            Connection conn = DriverManager.getConnection(path, username, password);
            try
            {
                String myqry = "select * from usertable where username = ?";
                PreparedStatement st = conn.prepareStatement(myqry); 
                st.setString(1,uname);   
                ResultSet rst = st.executeQuery();
                clearPage();
                if(rst.next())
                {
                    String c1 = rst.getString("name");
                    String c2 = rst.getString("phone");
                    String c3 = rst.getString("gender");
                    Date c4 = rst.getDate("dob");
                    String c5 = rst.getString("address");
                    String c6 = rst.getString("username");
                    String c7 = rst.getString("password");
                    String c8 = rst.getString("usertype"); 
                    
                    jTextField1.setText(c1);
                    jTextField2.setText(c2);
                    if(c3.equals("male"))
                        jRadioButton1.setSelected(true);
                    else
                        jRadioButton2.setSelected(true); 
                    
                    jDateChooser1.setDate(c4);
                    jTextArea1.setText(c5);
                    jTextField5.setText(c6);
                    jPasswordField1.setText(c7);
                    jComboBox1.setSelectedItem(c8); 
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "No Record Found");
                }  
            } 
            catch (Exception ex)
            {
                JOptionPane.showMessageDialog(this, "Error in search Query : "+ex.getMessage());
            } 
        } 
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Error in Connecting Database : "+ex.getMessage()); 
        }
}

