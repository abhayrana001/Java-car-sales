 boolean check_validation() 
    {
        if(jTextField1.getText().length()<2)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter proper name");
            return false;
        }
        else if(jTextField2.getText().length()<7 || jTextField2.getText().length()>15)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter Valid phone");
            return false;
        }
        else if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false)
        {
            JOptionPane.showMessageDialog(rootPane, "Please choose gender");
            return false;
        }
        else if(jDateChooser1.getDate()==null)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter DOB");
            return false;
        }
        else if(jTextArea1.getText().length()<1)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter Address");
            return false;
        }
         else if(jTextField5.getText().length()<2)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter username");
            return false;
        }
        else if(jPasswordField1.getText().length()<2)
        { 
            JOptionPane.showMessageDialog(rootPane, "Please enter password");
            return false;
        }
        else if(jComboBox1.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(rootPane, "Please choose user type");
            return false;
        }
        return true;
    } 
    
